    ocean.addColorStop(0,    '#55d4ff');
    ocean.addColorStop(.30,  '#0d72bb');
    ocean.addColorStop(.75,  '#062040');
    ocean.addColorStop(1,    '#020c1e');
    ctx.fillStyle = ocean;
