    atm.addColorStop(0, 'transparent');
    atm.addColorStop(1, 'rgba(100,200,255,.22)');
    ctx.fillStyle = atm;
    ctx.fillRect(0, 0, SIZE, SIZE);

    ctx.restore();

    /* Night-side shadow */
    ctx.save();
    ctx.beginPath();
    ctx.arc(cx, cy, R, 0, Math.PI * 2);
    ctx.clip();
    const night = ctx.createRadialGradient(cx + R * .5, cy, 0, cx + R * .35, cy, R * 1.15);
